"""Benchmark report rendering."""

from multi_agent_research_lab.core.schemas import BenchmarkMetrics


def render_markdown_report(metrics: list[BenchmarkMetrics]) -> str:
    """Render benchmark metrics to a detailed markdown report."""

    lines = [
        "# Benchmark Report",
        "",
        "## Summary Table",
        "",
        "| Run | Latency (s) | Cost (USD) | Quality (/10) | Notes |",
        "|---|---:|---:|---:|---|",
    ]

    for item in metrics:
        cost = "" if item.estimated_cost_usd is None else f"{item.estimated_cost_usd:.4f}"
        quality = "" if item.quality_score is None else f"{item.quality_score:.1f}"
        lines.append(
            f"| {item.run_name} | {item.latency_seconds:.2f} | {cost} | {quality} | {item.notes} |"
        )

    # Comparison analysis if we have at least 2 runs
    if len(metrics) >= 2:
        baseline = metrics[0]
        multi = metrics[1]

        lines.extend([
            "",
            "## Comparison Analysis",
            "",
        ])

        # Latency comparison
        if baseline.latency_seconds > 0:
            ratio = multi.latency_seconds / baseline.latency_seconds
            lines.append(f"- **Latency**: Multi-agent is {ratio:.1f}x the baseline ({multi.latency_seconds:.2f}s vs {baseline.latency_seconds:.2f}s)")

        # Cost comparison
        if baseline.estimated_cost_usd and multi.estimated_cost_usd:
            cost_ratio = multi.estimated_cost_usd / baseline.estimated_cost_usd
            lines.append(f"- **Cost**: Multi-agent costs {cost_ratio:.1f}x the baseline (${multi.estimated_cost_usd:.4f} vs ${baseline.estimated_cost_usd:.4f})")

        # Quality comparison
        if baseline.quality_score is not None and multi.quality_score is not None:
            diff = multi.quality_score - baseline.quality_score
            direction = "higher" if diff > 0 else "lower" if diff < 0 else "equal"
            lines.append(f"- **Quality**: Multi-agent scores {abs(diff):.1f} points {direction} ({multi.quality_score:.1f} vs {baseline.quality_score:.1f})")

        lines.extend([
            "",
            "## Trade-off Summary",
            "",
            "Multi-agent systems typically trade increased latency and cost for:",
            "- Better source coverage and citation quality",
            "- Structured analysis with cross-referencing",
            "- Separation of concerns (research vs analysis vs writing)",
            "- More robust error handling through agent specialization",
        ])

    lines.extend(["", "---", f"*Generated by multi-agent-research-lab benchmark*", ""])

    return "\n".join(lines)
